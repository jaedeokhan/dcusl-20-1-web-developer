package _0418SAT_BankMenu;

public class ForEx {

	public static void main(String[] args) {
		/*
		 표현)
		 	for( 변수 선언(초기화); 조건식; 증감혹은 감소 수식){
		 		실행 구문;
		 	}
		 */
		
		int i = 0;
		while(i < 10) {
			System.out.println("count : " + i);
			i++;
		}
		System.out.println("========================");
		
		for (int count = 0; count < 10; count++) {
			System.out.println("count : " + count);
		}

	}

}
