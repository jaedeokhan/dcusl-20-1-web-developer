package vo;

public class ZipCodeVO {
   private int num;
   private String zip;
   private String dou;
   private String si;
   private String ro;
   private String bunzi;
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}
public String getZip() {
	return zip;
}
public void setZip(String zip) {
	this.zip = zip;
}
public String getDou() {
	return dou;
}
public void setDou(String dou) {
	this.dou = dou;
}
public String getSi() {
	return si;
}
public void setSi(String si) {
	this.si = si;
}
public String getRo() {
	return ro;
}
public void setRo(String ro) {
	this.ro = ro;
}
public String getBunzi() {
	return bunzi;
}
public void setBunzi(String bunzi) {
	this.bunzi = bunzi;
}
   
}
